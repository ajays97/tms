    private void loadDetails() {
        
        l_description.setLineWrap(true);
        l_description.setEditable(false);
        l_description.setBackground(new Color(0, 0, 0, 0));
        list_packages.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                int index = list_packages.getSelectedIndex()+1;
                l_pname.setText(pname[index]);
                l_description.setText(description[index]);
                l_price.setText(price[index]+"/-");
                l_pid.setText(""+pid[index]);
            }
        });
        String sql = "SELECT * FROM packages;";
        try {
            rs = stmt.executeQuery(sql);
            int i = 1;
            int len = rs.getMetaData().getColumnCount();
            description = new String[len+1];
            price = new Integer[len+1];
            pname = new String[len+1];
            pid = new Integer[len+1];
            while(rs.next()) {                
                description[i] = rs.getString("description");
                price[i] = rs.getInt("price");
                pid[i] = rs.getInt("pid");
                pname[i] = rs.getString("pname");
                list_packages.addItem(rs.getString("pname"));
                i++;
            }            
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        String sql2 = "SELECT pid, pname from packages where pid = (select pid from bookings where email = '" + email + "');";
        try {
            rs = stmt.executeQuery(sql2);
            t_bookings.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(Home.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

