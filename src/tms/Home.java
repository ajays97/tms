    private void btn_feedbackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_feedbackActionPerformed
        if (ta_feedback.getText().equals(""))
            JOptionPane.showMessageDialog(new JFrame(), "Feedback can't be empty.");
        else
            try {
                stmt = conn.createStatement();
                String query = "INSERT INTO feedbacks values('" + username + "','" + ta_feedback.getText() + "');";
                stmt.executeUpdate(query);
                JOptionPane.showMessageDialog(new JFrame(), "Feedback Submitted.");
            } catch (SQLException ex) {
                Logger.getLogger(TMSUI.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btn_feedbackActionPerformed

